/* ---- Favoris : persistance localStorage + seed déterministe ---- */
(function () {
  var KEY = "bcr_favs_v1";
  var D = window.BCR_DATA;

  function seed() {
    // ~28 % de la watchlist en favoris par défaut (déterministe sur l'id de série)
    var ids = [];
    (D.watchlist || []).forEach(function (w) {
      var s = String(w.seriesId || w.seriesTitle || "");
      var h = 0;
      for (var i = 0; i < s.length; i++) h = (h * 31 + s.charCodeAt(i)) >>> 0;
      if (h % 100 < 28) ids.push(w.seriesId);
    });
    return ids;
  }

  function read() {
    try {
      var raw = localStorage.getItem(KEY);
      if (raw) return JSON.parse(raw);
    } catch (e) {}
    var s = seed();
    try { localStorage.setItem(KEY, JSON.stringify(s)); } catch (e) {}
    return s;
  }

  window.BCRFavs = {
    all: function () { return read(); },
    has: function (id) { return read().indexOf(id) !== -1; },
    toggle: function (id) {
      var list = read();
      var i = list.indexOf(id);
      if (i === -1) list.push(id); else list.splice(i, 1);
      try { localStorage.setItem(KEY, JSON.stringify(list)); } catch (e) {}
      return list.indexOf(id) !== -1;
    },
    count: function () { return read().length; }
  };
})();
